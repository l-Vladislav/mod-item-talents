-- row 2); row 3 is now a single shared menu across all pools (pool E swaps
-- "Легкость" for "Стойкость"); row 4 unchanged. `choice` is a menu-option
-- id, NOT the roll slot (1..3) — the item only rolls 3 choices per row at
-- first access and stores them in acore_characters.item_talent_rolls
-- (see pending_db_characters/mod_item_talents_rolls.sql).
-- Idempotent: CREATE TABLE IF NOT EXISTS + DELETE FROM + full re-INSERT.

