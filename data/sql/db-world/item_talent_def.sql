  `choice`   TINYINT      NOT NULL,  -- id варианта в меню ряда (не то же самое,
                                     -- что слот 1..3, который реально роллится)
  `name_ru`  VARCHAR(64)  NOT NULL,
  `desc_ru`  VARCHAR(255) NOT NULL,  -- {N} подставляется модулем
  `effect`   VARCHAR(32)  NOT NULL,
  `base`     FLOAT NOT NULL DEFAULT 0,
  `per_ilvl` FLOAT NOT NULL DEFAULT 0,
